# ── Parser ────────────────────────────────────────────────────────────────────

def parse_key_block(text: str) -> dict:
    """
    Parse a raw paste of keys in any format:
      KEY=value
      ✓ KEY=value
      KEY: value
      value (detected by pattern)
    Returns dict of {KEY_NAME: value}.
    """
    found = {}
    lines = text.strip().splitlines()

    for line in lines:
        line = line.strip().lstrip("✓ ").strip()

        # Try KEY=value or KEY: value
        for sep in ["=", ":"]:
            if sep in line:
                parts = line.split(sep, 1)
                key_candidate = parts[0].strip().upper().replace(" ", "_")
                val = parts[1].strip()

                # Normalize key names
                key_map = {
                    "TWILIO_PHONE": "TWILIO_PHONE_NUMBER",
                    "ALPACA_BROKER": "ALPACA_API_KEY",
                    "SECRET": "ALPACA_API_SECRET",
                    "TWITTER": None,  # skip
                }
                if key_candidate in key_map:
                    key_candidate = key_map[key_candidate]

                if key_candidate and key_candidate in KEY_PATTERNS and val:
                    found[key_candidate] = val
                break

        # Try pattern matching for values without key names
        for key_name, pattern in KEY_PATTERNS.items():
            if key_name not in found:
                m = re.search(pattern, line)
                if m:
                    # Don't double-assign
                    val = m.group(0)
                    if val not in found.values():
                        found[key_name] = val

    # Clean phone number
    if "TWILIO_PHONE_NUMBER" in found:
        phone = re.sub(r"[^\d+]", "", found["TWILIO_PHONE_NUMBER"])
        if not phone.startswith("+"):
            phone = "+" + phone
        found["TWILIO_PHONE_NUMBER"] = phone

    return found


def validate_keys(keys: dict) -> dict:
    """Returns {key: "ok"|"missing"|"invalid"}"""
    results = {}
    for key in list(KEY_PATTERNS.keys()):
        if key not in keys:
            results[key] = "missing" if key in REQUIRED else "optional"
        else:
            pattern = KEY_PATTERNS[key]
            if re.search(pattern, keys[key]):
                results[key] = "ok"
            else:
                results[key] = "invalid"
    return results


