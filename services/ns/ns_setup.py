def store_in_keychain(key_name: str, value: str):
    """Store a key in macOS Keychain as backup."""
    try:
        subprocess.run([
            "security", "add-generic-password",
            "-a", "northstar",
            "-s", f"northstar.{key_name.lower()}",
            "-w", value,
            "-U"  # update if exists
        ], capture_output=True, check=False)
    except Exception:
        pass  # Keychain optional


def store_all_in_keychain(keys: dict):
    """Store all keys in macOS Keychain."""
    for k, v in keys.items():
        if v:
            store_in_keychain(k, v)

